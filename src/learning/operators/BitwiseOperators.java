package learning.operators;

public class BitwiseOperators {

	public static void main(String[] args) {
		//Initial Values
		int a=5;
		int b=7;
		
		//bitwise AND
		// 5=> 0101 & 7=> 0111 = 0101 =>5
		System.out.println("a&b => "+(a&b));
		
		//bitwise OR
		//5=> 0101 | 7=> 0111 = 0111 =>7
		System.out.println("a|b => "+(a|b));

	}

}
